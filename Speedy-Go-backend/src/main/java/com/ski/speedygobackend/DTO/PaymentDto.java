package com.ski.speedygobackend.DTO;

import com.ski.speedygobackend.Enum.PaymentMethod;

import java.util.Date;

public class PaymentDto {
    private Long paymentId;
    private Double amount;
    private Date paymentDate;
    private PaymentMethod paymentMethod;
    private Long userId;
    private String userEmail;

    // Constructor
    public PaymentDto(Long paymentId, Double amount, Date paymentDate, PaymentMethod paymentMethod, Long userId, String userEmail) {
        this.paymentId = paymentId;
        this.amount = amount;
        this.paymentDate = paymentDate;
        this.paymentMethod = paymentMethod;
        this.userId = userId;
        this.userEmail = userEmail;
    }

    // Getters and setters
    public Long getPaymentId() {
        return paymentId;
    }

    public void setPaymentId(Long paymentId) {
        this.paymentId = paymentId;
    }

    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }

    public Date getPaymentDate() {
        return paymentDate;
    }

    public void setPaymentDate(Date paymentDate) {
        this.paymentDate = paymentDate;
    }

    public PaymentMethod getPaymentMethod() {
        return paymentMethod;
    }

    public void setPaymentMethod(PaymentMethod paymentMethod) {
        this.paymentMethod = paymentMethod;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }
}